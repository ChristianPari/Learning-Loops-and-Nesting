//example of while loops

/*  
> only has the conditional statement, and it will execute the code while that condition is true within the code block '{}' 
*/

let number = 0;
// print 0 - 20 to the console using a while loop

while (number <= 20) { // assigns the conditional to be executed while 'number' is less than or equal to 20, which it will then stop
    console.log(number); // having number output
    number++; // will make 'number' increment by 1 each time the code block (logs 'number' to the console) is executed and put back into the while loop
}